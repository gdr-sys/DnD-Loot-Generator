export function getGemName(name: string, value: number, lang: 'en' | 'it'): string {
  if (lang === 'it' && gemsIT[value]) {
    return gemsIT[value][name] || name;
  }
  return name;
}

export function getArtName(name: string, value: number, lang: 'en' | 'it'): string {
  if (lang === 'it' && artObjectsIT[value]) {
    return artObjectsIT[value][name] || name;
  }
  return name;
}
